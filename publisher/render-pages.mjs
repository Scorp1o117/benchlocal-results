#!/usr/bin/env node
import fs from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { assert, parseArgs, readJson, round1 } from "./lib.mjs";

const GENERATED_MARKER = "<!-- Generated by publisher/render-pages.mjs; do not edit scores by hand. -->";
const REDIRECT_MARKER = "<!-- Generated legacy redirect by publisher/render-pages.mjs. -->";

function escapeHtml(value) {
  return String(value ?? "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

function number(value) {
  const rounded = Number(Number(value).toFixed(6));
  return String(Object.is(rounded, -0) ? 0 : rounded);
}

function signed(value) {
  const normalized = Number(Number(value).toFixed(6));
  return `${normalized > 0 ? "+" : ""}${number(normalized)}`;
}

function modeLabel(variant) {
  return variant === "thinking"
    ? { zh: "思考模式", en: "Thinking mode" }
    : variant === "no-thinking"
      ? { zh: "无思考模式", en: "No-thinking mode" }
      : { zh: "默认模式", en: "Default mode" };
}

function suiteCard(title, suite) {
  return `<article class="suite-card"><span>${title}</span><strong>${number(suite.totalScore)}</strong><div class="score-track suite-track" aria-hidden="true"><i style="width:${Math.max(0, Math.min(100, Number(suite.totalScore)))}%"></i></div><small><span data-lang="zh">首次 ${number(suite.initialTotalScore)} · 重试 ${suite.retryPenalty}</span><span data-lang="en">First ${number(suite.initialTotalScore)} · ${suite.retryPenalty} retries</span></small></article>`;
}

function metadata(snapshot) {
  const meta = snapshot.editorial?.metadata ?? {};
  const items = [];
  if (meta.modelFile) items.push(`📄 ${escapeHtml(meta.modelFile)}`);
  if (meta.fileSizeGb !== undefined) items.push(`📦 ${number(meta.fileSizeGb)} GB`);
  if (meta.speed) items.push(`⚡ In ${number(meta.speed.inputTokensPerSecond)} t/s · Out ${number(meta.speed.outputTokensPerSecond)} t/s`);
  if (meta.sampling) items.push(`🌡 temp=${number(meta.sampling.temperature)} · top_p=${number(meta.sampling.topP)} · top_k=${number(meta.sampling.topK)}`);
  for (const tag of meta.displayTags ?? []) if (tag && !items.some((item) => item.includes(tag))) items.push(escapeHtml(tag));
  if (meta.testedAt) items.push(`📅 ${escapeHtml(meta.testedAt)}`);
  return items.map((item) => `<span>${item}</span>`).join("");
}

function modelLogo(meta, displayName) {
  const source = meta.logoPath ?? meta.logoUrl;
  if (source) {
    const resolvedSource = source.startsWith("icons/") ? `../${source}` : source;
    return `<div class="detail-logo"><img src="${escapeHtml(resolvedSource)}" alt="${escapeHtml(displayName)} logo"></div>`;
  }
  return `<div class="detail-logo placeholder" data-logo-slot="true" role="img" aria-label="${escapeHtml(displayName)} logo placeholder"><span>LOGO</span></div>`;
}

function sourceLinks(meta) {
  const links = [];
  if (meta.artifactUrl) {
    const label = meta.artifactKind === "api" ? { zh: "测试服务链接", en: "Tested service" } : { zh: "测试模型链接", en: "Tested model" };
    links.push(`<a href="${escapeHtml(meta.artifactUrl)}" target="_blank" rel="noopener noreferrer"><span data-lang="zh">${label.zh}</span><span data-lang="en">${label.en}</span> ↗</a>`);
  }
  if (meta.upstreamUrl) links.push(`<a href="${escapeHtml(meta.upstreamUrl)}" target="_blank" rel="noopener noreferrer"><span data-lang="zh">上游模型</span><span data-lang="en">Upstream model</span> ↗</a>`);
  if (meta.publisherUrl && /huggingface\.co\/SC117\/?$/i.test(meta.publisherUrl)) links.push(`<a href="${escapeHtml(meta.publisherUrl)}" target="_blank" rel="noopener noreferrer"><span data-lang="zh">发布者 SC117</span><span data-lang="en">Publisher SC117</span> ↗</a>`);
  return links.length ? `<div class="source-links">${links.join("")}</div>` : "";
}

function resultRows(snapshot) {
  return Object.values(snapshot.suites).flatMap((suite) => suite.results.map((result) => {
    const resultClass = result.status === "pass" ? "pass" : result.status === "partial" ? "partial" : "fail";
    return `<tr><td>${escapeHtml(result.id)}</td><td>${escapeHtml(suite.packId)}</td><td class="${resultClass}">${number(result.score)}</td><td>${result.attempts}</td><td>${result.retries}</td><td>${escapeHtml(result.summary)}</td></tr>`;
  })).join("\n");
}

function provenance(snapshot) {
  return Object.values(snapshot.suites).map((suite) => `<li><strong>${escapeHtml(suite.packId)}</strong> · <code>${escapeHtml(suite.runId)}</code> · SHA-256 <code>${escapeHtml(suite.source.sha256.slice(0, 16))}…</code></li>`).join("");
}

function variantSection(snapshot, index) {
  const label = modeLabel(snapshot.model.variant);
  const scoring = snapshot.scoring;
  const summary = snapshot.editorial?.summary;
  const verdict = snapshot.editorial?.verdict;
  const editorial = summary?.zh || summary?.en || verdict?.zh || verdict?.en
    ? `<div class="editorial"><p><span data-lang="zh">${escapeHtml(summary?.zh)}</span><span data-lang="en">${escapeHtml(summary?.en)}</span></p><p class="verdict"><span data-lang="zh">${escapeHtml(verdict?.zh)}</span><span data-lang="en">${escapeHtml(verdict?.en)}</span></p></div>`
    : "";
  return `<section class="variant reveal" id="${escapeHtml(snapshot.model.variant)}">
    <header class="variant-heading"><div><span class="section-index">${String(index + 1).padStart(2, "0")} / Benchmark mode</span><span class="mode-badge ${escapeHtml(snapshot.model.variant)}"><span data-lang="zh">${label.zh}</span><span data-lang="en">${label.en}</span></span><h2><span data-lang="zh">评测结果</span><span data-lang="en">Benchmark results</span></h2></div><a href="#top"><span data-lang="zh">返回顶部</span><span data-lang="en">Back to top</span> ↑</a></header>
    <div class="score-grid">
      <article class="score-card max"><span><span data-lang="zh">能力上限</span><span data-lang="en">Max score</span></span><strong>${number(scoring.maxScore)}</strong><div class="score-track" aria-hidden="true"><i style="width:${Math.max(0, Math.min(100, Number(scoring.maxScore)))}%"></i></div><small>TC × 0.3 + BF × 0.3 + HA × 0.4</small></article>
      <article class="score-card effective"><span><span data-lang="zh">实用得分</span><span data-lang="en">Effective score</span></span><strong>${number(scoring.effectiveScore)}</strong><div class="score-track" aria-hidden="true"><i style="width:${Math.max(0, Math.min(100, Number(scoring.effectiveScore)))}%"></i></div><small><span data-lang="zh">重试扣分 −${scoring.retryPenalty}</span><span data-lang="en">Retry penalty −${scoring.retryPenalty}</span></small></article>
      <article class="score-card stability"><span><span data-lang="zh">最终结果</span><span data-lang="en">Outcomes</span></span><strong>${scoring.counts.pass}/50</strong><div class="score-track" aria-hidden="true"><i style="width:${Math.max(0, Math.min(100, scoring.counts.pass * 2))}%"></i></div><small>✓ ${scoring.counts.pass} · ◐ ${scoring.counts.partial} · × ${scoring.counts.fail}</small></article>
    </div>
    <div class="suite-grid">${suiteCard("ToolCall-15", snapshot.suites.toolcall)}${suiteCard("BugFind-15", snapshot.suites.bugfind)}${suiteCard("HermesAgent-20", snapshot.suites.hermes)}</div>
    <div class="metadata">${metadata(snapshot)}</div>
    ${editorial}
    <details><summary><span data-lang="zh">查看全部 50 题结果</span><span data-lang="en">View all 50 scenario results</span><span>＋</span></summary><div class="table-wrap"><table><thead><tr><th>ID</th><th>Suite</th><th><span data-lang="zh">得分</span><span data-lang="en">Score</span></th><th>Attempts</th><th>Retries</th><th><span data-lang="zh">判定摘要</span><span data-lang="en">Result summary</span></th></tr></thead><tbody>${resultRows(snapshot)}</tbody></table></div></details>
    <details><summary><span data-lang="zh">数据来源与校验</span><span data-lang="en">Provenance and verification</span><span>＋</span></summary><ul class="provenance">${provenance(snapshot)}</ul></details>
  </section>`;
}

function renderPage(snapshots) {
  const sorted = snapshots.slice().sort((a, b) => (a.model.variant === "thinking" ? -1 : b.model.variant === "thinking" ? 1 : a.model.variant.localeCompare(b.model.variant)));
  const byVariant = Object.fromEntries(sorted.map((snapshot) => [snapshot.model.variant, snapshot]));
  const thinking = byVariant.thinking;
  const noThinking = byVariant["no-thinking"];
  const comparison = thinking && noThinking ? {
    max: round1(thinking.scoring.maxScore - noThinking.scoring.maxScore),
    effective: round1(thinking.scoring.effectiveScore - noThinking.scoring.effectiveScore),
    retries: thinking.scoring.retryPenalty - noThinking.scoring.retryPenalty,
    tc: round1(thinking.suites.toolcall.totalScore - noThinking.suites.toolcall.totalScore),
    bf: round1(thinking.suites.bugfind.totalScore - noThinking.suites.bugfind.totalScore),
    ha: round1(thinking.suites.hermes.totalScore - noThinking.suites.hermes.totalScore),
  } : null;
  const modelMeta = sorted[0].editorial?.metadata ?? {};
  const slug = sorted[0].model.slug;
  const displayName = modelMeta.displayName ?? sorted[0].model.label;
  const modelFile = modelMeta.modelFile ?? sorted[0].model.label;
  const switches = sorted.map((snapshot) => {
    const label = modeLabel(snapshot.model.variant);
    return `<a href="#${escapeHtml(snapshot.model.variant)}"><span class="switch-dot ${escapeHtml(snapshot.model.variant)}"></span><span data-lang="zh">${label.zh}</span><span data-lang="en">${label.en}</span><strong>${number(snapshot.scoring.maxScore)}</strong></a>`;
  }).join("");
  const comparisonHtml = comparison ? `<section class="comparison-strip reveal"><div><span><span data-lang="zh">思考版能力上限</span><span data-lang="en">Thinking max</span></span><strong>${signed(comparison.max)}</strong></div><div><span><span data-lang="zh">实用分差</span><span data-lang="en">Effective delta</span></span><strong>${signed(comparison.effective)}</strong></div><div><span><span data-lang="zh">额外重试</span><span data-lang="en">Extra retries</span></span><strong>${signed(comparison.retries)}</strong></div><div><span>TC / BF / HA</span><strong>${signed(comparison.tc)} / ${signed(comparison.bf)} / ${signed(comparison.ha)}</strong></div></section>` : "";
  const comparisonBlock = comparisonHtml ? `    ${comparisonHtml}\n` : "";
  return `<!DOCTYPE html>
<html lang="zh-CN" id="top">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <meta name="description" content="${escapeHtml(displayName)} BenchLocal-Results benchmark results">
  <meta name="theme-color" content="#f4f6fa">
  <title>${escapeHtml(displayName)} · BenchLocal-Results</title>
  <link rel="icon" type="image/png" href="../icons/benchlocal-results-logo.png">
  <link rel="canonical" href="https://scorp1o117.github.io/benchlocal-results/models/${escapeHtml(slug)}.html">
  <link rel="stylesheet" href="../styles/detail.css">
  <script src="../scripts/detail.js" defer></script>
</head>
<body>${GENERATED_MARKER}
  <nav class="detail-nav"><div><a class="detail-brand" href="../index.html"><span><img src="../icons/benchlocal-results-logo.png" alt=""></span>BenchLocal-Results</a><div class="detail-actions"><a href="../index.html#leaderboard"><span data-lang="zh">排行榜</span><span data-lang="en">Leaderboard</span></a><div class="lang-toggle" role="group" aria-label="Language"><button type="button" data-lb="zh" aria-pressed="true">中</button><button type="button" data-lb="en" aria-pressed="false">EN</button></div></div></div></nav>
  <main>
    <header class="detail-hero reveal"><div class="hero-visual"><div class="hero-halo"></div>${modelLogo(modelMeta, displayName)}</div><div class="hero-content"><span class="hero-kicker">${thinking && noThinking ? "Dual-mode benchmark" : "Model benchmark"}</span><h1>${escapeHtml(displayName)}</h1><p class="model-file">${escapeHtml(modelFile)}</p><p class="hero-summary"><span data-lang="zh">${thinking && noThinking ? "同一模型在思考与无思考模式下的能力上限、实际稳定性和完整 50 题结果。" : "模型的能力上限、实际稳定性和完整 50 题结果。"}</span><span data-lang="en">${thinking && noThinking ? "Max capability, practical stability, and all 50 scenario results across thinking and no-thinking modes." : "Max capability, practical stability, and all 50 scenario results."}</span></p><div class="mode-links">${switches}</div>${sourceLinks(modelMeta)}</div></header>
${comparisonBlock}    <div class="detail-content">${sorted.map(variantSection).join("\n")}</div>
  </main>
  <footer><a href="../index.html">BenchLocal-Results</a><span><span data-lang="zh">数据由规范化快照自动生成</span><span data-lang="en">Generated from normalized benchmark snapshots</span></span><small>© 2026</small></footer>
</body>
</html>\n`;
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const cwd = process.cwd();
  const snapshotDirectory = path.resolve(cwd, typeof args.snapshots === "string" ? args.snapshots : "publisher/data/models");
  const outputDirectory = path.resolve(cwd, typeof args.output === "string" ? args.output : "models");
  const files = (await fs.readdir(snapshotDirectory)).filter((name) => name.endsWith(".json")).sort();
  const groups = new Map();
  for (const file of files) {
    const snapshot = (await readJson(path.join(snapshotDirectory, file))).data;
    const group = groups.get(snapshot.model.slug) ?? [];
    group.push(snapshot);
    groups.set(snapshot.model.slug, group);
  }
  let rendered = 0;
  let redirects = 0;
  for (const [slug, snapshots] of groups) {
    const outputPath = path.join(outputDirectory, `${slug}.html`);
    const expected = renderPage(snapshots);
    const actual = await fs.readFile(outputPath, "utf8").catch(() => null);
    if (args.check) {
      assert(actual === expected, `${path.relative(cwd, outputPath)} is stale; run npm run build:pages.`);
    } else {
      if (actual !== null) assert(actual.includes(GENERATED_MARKER) || snapshots.some((snapshot) => snapshot.editorial?.metadata?.pageMode === "generated"), `${outputPath} exists but is not a generated page.`);
      await fs.mkdir(path.dirname(outputPath), { recursive: true });
      await fs.writeFile(outputPath, expected, "utf8");
    }
    rendered += 1;
    for (const snapshot of snapshots) {
      const legacyPage = snapshot.editorial?.metadata?.legacyPage?.split("#", 1)[0];
      if (!legacyPage) continue;
      const legacyPath = path.resolve(cwd, legacyPage);
      if (legacyPath === outputPath) continue;
      const target = `${path.basename(outputPath)}#${snapshot.model.variant}`;
      const redirect = `<!DOCTYPE html>\n<html lang="zh-CN"><head><meta charset="UTF-8"><meta name="viewport" content="width=device-width,initial-scale=1.0"><meta http-equiv="refresh" content="0;url=${escapeHtml(target)}"><title>${escapeHtml(snapshot.editorial?.metadata?.displayName ?? snapshot.model.label)} · BenchLocal-Results</title></head><body>${REDIRECT_MARKER}<p><a href="${escapeHtml(target)}">前往合并详情页 / Open merged details</a></p><script>location.replace(${JSON.stringify(target)});</script></body></html>\n`;
      const existing = await fs.readFile(legacyPath, "utf8").catch(() => null);
      if (args.check) assert(existing === redirect, `${path.relative(cwd, legacyPath)} redirect is stale; run npm run build:pages.`);
      else await fs.writeFile(legacyPath, redirect, "utf8");
      redirects += 1;
    }
  }
  console.log(`${args.check ? "Model pages are current" : "Model pages rendered"}: ${rendered} canonical pages, ${redirects} legacy redirects.`);
}

main().catch((error) => {
  console.error(`page render failed: ${error.message}`);
  process.exitCode = 1;
});
