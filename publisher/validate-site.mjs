#!/usr/bin/env node
import fs from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import vm from "node:vm";
import { assert } from "./lib.mjs";

const siteRoot = process.cwd();

function allMatches(text, regex) {
  return [...text.matchAll(regex)];
}

function checkScriptSyntax(html, relativePath) {
  const scripts = allMatches(html, /<script(?:\s[^>]*)?>([\s\S]*?)<\/script>/gi);
  for (let index = 0; index < scripts.length; index += 1) {
    const openingTag = scripts[index][0].slice(0, scripts[index][0].indexOf(">") + 1);
    if (/\bsrc\s*=/.test(openingTag)) continue;
    new vm.Script(scripts[index][1], { filename: `${relativePath}#script-${index + 1}` });
  }
}

function duplicateIds(html) {
  const ids = allMatches(html, /\bid=["']([^"']+)["']/gi).map((match) => match[1]);
  const seen = new Set();
  return ids.filter((id) => (seen.has(id) ? true : !seen.add(id)));
}

function internalLinks(html) {
  return allMatches(html, /\b(?:href|src)=["']([^"']+)["']/gi)
    .map((match) => match[1])
    .filter((link) => !/^(?:https?:|mailto:|tel:|data:|javascript:|#)/i.test(link));
}

function cssAssetLinks(css) {
  return allMatches(css, /url\(\s*["']?([^"')]+)["']?\s*\)/gi).map((match) => match[1]);
}

async function pathExists(fromFile, link) {
  const clean = decodeURIComponent(link.split(/[?#]/, 1)[0]);
  if (!clean) return true;
  try {
    await fs.access(path.resolve(path.dirname(fromFile), clean));
    return true;
  } catch {
    return false;
  }
}

function undefinedCssVariables(html) {
  const definitions = new Set(allMatches(html, /(--[a-zA-Z0-9_-]+)\s*:/g).map((match) => match[1]));
  const usages = new Set(allMatches(html, /var\(\s*(--[a-zA-Z0-9_-]+)/g).map((match) => match[1]));
  return [...usages].filter((name) => !definitions.has(name));
}

async function main() {
  const indexPath = path.join(siteRoot, "index.html");
  const modelDirectory = path.join(siteRoot, "models");
  const htmlFiles = [indexPath, path.join(siteRoot, "changelog.html")];
  const modelFiles = (await fs.readdir(modelDirectory))
    .filter((name) => name.endsWith(".html"))
    .sort()
    .map((name) => path.join(modelDirectory, name));
  htmlFiles.push(...modelFiles);

  const errors = [];
  const warnings = [];
  for (const file of htmlFiles) {
    const relative = path.relative(siteRoot, file).replaceAll("\\", "/");
    const html = await fs.readFile(file, "utf8");
    try {
      checkScriptSyntax(html, relative);
    } catch (error) {
      errors.push(`${relative}: ${error.message}`);
    }
    for (const id of duplicateIds(html)) errors.push(`${relative}: duplicate id '${id}'.`);
    for (const match of allMatches(html, /<img\b[^>]*>/gi)) {
      if (!/\balt=["'][^"']*["']/i.test(match[0])) errors.push(`${relative}: image is missing an alt attribute.`);
    }
    for (const match of allMatches(html, /<(?:img|script|source)\b[^>]*\bsrc=["'](https?:\/\/[^"']+)["']/gi)) {
      errors.push(`${relative}: external runtime asset '${match[1]}' must be vendored locally.`);
    }
    for (const match of allMatches(html, /<a\b[^>]*\btarget=["']_blank["'][^>]*>/gi)) {
      if (!/\brel=["'][^"']*\bnoopener\b[^"']*\bnoreferrer\b[^"']*["']/i.test(match[0])) {
        errors.push(`${relative}: target=_blank link must include rel="noopener noreferrer".`);
      }
    }
    if (html.includes("Generated by publisher/render-pages.mjs") && />[+-]?\d+\.\d{6,}</.test(html)) {
      errors.push(`${relative}: generated page contains an unformatted floating-point value.`);
    }
    for (const variable of undefinedCssVariables(html)) warnings.push(`${relative}: CSS variable ${variable} is used but not defined in this file.`);
    for (const link of internalLinks(html)) {
      if (!(await pathExists(file, link))) errors.push(`${relative}: broken local link '${link}'.`);
    }
  }

  const styleDirectory = path.join(siteRoot, "styles");
  const styleFiles = (await fs.readdir(styleDirectory)).filter((name) => name.endsWith(".css")).sort();
  for (const name of styleFiles) {
    const file = path.join(styleDirectory, name);
    const relative = path.relative(siteRoot, file).replaceAll("\\", "/");
    const css = await fs.readFile(file, "utf8");
    if (/@import\s+(?:url\()?\s*["']?https?:\/\//i.test(css)) errors.push(`${relative}: remote CSS imports are not allowed.`);
    for (const link of cssAssetLinks(css)) {
      if (/^(?:data:|#)/i.test(link)) continue;
      if (/^https?:\/\//i.test(link)) errors.push(`${relative}: external runtime asset '${link}' must be vendored locally.`);
      else if (!(await pathExists(file, link))) errors.push(`${relative}: broken local asset '${link}'.`);
    }
  }

  for (const name of ["site.js", "detail.js"]) {
    const file = path.join(siteRoot, "scripts", name);
    new vm.Script(await fs.readFile(file, "utf8"), { filename: `scripts/${name}` });
  }

  const indexHtml = await fs.readFile(indexPath, "utf8");
  const cardMatches = allMatches(indexHtml, /<a class=["'][^"']*\bmodel-card\b[^"']*["'][^>]*>/gi);
  const cardTags = cardMatches.map((match) => match[0]);
  const cardLinks = allMatches(indexHtml, /<a class=["'][^"']*\bmodel-card\b[^"']*["'][^>]*\bhref=["']([^"']+)["']/gi).map(
    (match) => match[1],
  );
  const modelNames = cardTags.map((tag) => tag.match(/\bdata-model-name=["']([^"']+)["']/i)?.[1] ?? null);
  const modes = cardTags.map((tag) => tag.match(/\bdata-modes=["']([^"']+)["']/i)?.[1]?.split(",") ?? null);
  const metrics = cardTags.map((tag) => ["total", "adjusted", "tc", "bf", "ha"].map((key) => Number(tag.match(new RegExp(`\\bdata-${key}=["']([^"']+)["']`, "i"))?.[1])));
  const modeMetricsValid = cardTags.every((tag, index) => modes[index]?.every((mode) => ["total", "adjusted", "tc", "bf", "ha"].every((key) => Number.isFinite(Number(tag.match(new RegExp(`\\bdata-${mode}-${key}=["']([^"']+)["']`, "i"))?.[1])))));
  if (cardTags.length !== cardLinks.length) errors.push(`index.html has ${cardTags.length} cards but ${cardLinks.length} card links.`);
  if (modelNames.some((name) => !name)) errors.push("index.html model cards must define data-model-name.");
  if (cardTags.some((tag) => !/\baria-label=["'][^"']+["']/i.test(tag))) errors.push("index.html model cards must define an accessible name.");
  if (modes.some((value) => !value || value.length < 1 || value.some((mode) => !["thinking", "no-thinking", "default"].includes(mode)))) errors.push("index.html model cards must define valid data-modes.");
  if (metrics.some((values) => values.some((value) => !Number.isFinite(value)))) errors.push("index.html model cards must define finite sortable metrics.");
  if (!modeMetricsValid) errors.push("index.html model cards must define finite sortable metrics for every included mode.");
  if (new Set(cardLinks).size !== cardLinks.length) errors.push("index.html contains duplicate model card links.");
  if (new Set(modelNames).size !== modelNames.length) errors.push("index.html model cards contain duplicate data-model-name values.");

  for (const link of cardLinks) {
    if (!(await pathExists(indexPath, link))) errors.push(`index.html card points to missing page '${link}'.`);
  }

  const linkedPages = new Set(cardLinks.map((link) => path.basename(link)));
  for (const file of modelFiles) {
    const name = path.basename(file);
    const html = await fs.readFile(file, "utf8");
    if (!linkedPages.has(name) && !html.includes("Generated legacy redirect by publisher/render-pages.mjs")) warnings.push(`models/${name} has no homepage card.`);
  }

  const heroCountMatch = indexHtml.match(/<div class="hero-stat"><div class="val">(\d+)<\/div><div class="lbl"><span data-lang="zh">模型/);
  if (heroCountMatch && Number(heroCountMatch[1]) !== cardTags.length) {
    errors.push(`Hero model count is ${heroCountMatch[1]}, expected ${cardTags.length}.`);
  }

  for (const warning of [...new Set(warnings)].sort()) console.warn(`warning: ${warning}`);
  if (errors.length > 0) throw new Error(`\n- ${[...new Set(errors)].sort().join("\n- ")}`);
  console.log(`Site validation passed: ${cardTags.length} cards, ${modelFiles.length} model pages, ${htmlFiles.length} HTML files.`);
}

main().catch((error) => {
  console.error(`site validation failed: ${error.message}`);
  process.exitCode = 1;
});
